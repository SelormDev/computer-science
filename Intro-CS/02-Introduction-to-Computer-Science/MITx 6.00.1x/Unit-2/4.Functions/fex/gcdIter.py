# gcd(17, 12) = 1

def gcdIter(a, b):
    '''
    a, b: positive integers
    
    returns: a positive integer, the greatest common divisor of a & b.
    '''
    # Your code here
    if a < b:
        num = a
    else:
        num = b

    while True:
        if (a % num == 0) and (b % num == 0):
            return num
        num = num -1

print(gcdIter(2, 12))
print(gcdIter(6, 12))
print(gcdIter(9, 12))
print(gcdIter(17, 12))
